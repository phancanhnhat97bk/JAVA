package BEAN;

public class Guideline {
	private int guidelineid;
	private String guidelinename;
	private String guidelineimage;
	private String content;
	private int guidelineroleid;
	private int userid;
	private int level;
	
	public int getGuidelineid() {
		return guidelineid;
	}
	public void setGuidelineid(int guidelineid) {
		this.guidelineid = guidelineid;
	}
	public String getGuidelinename() {
		return guidelinename;
	}
	public void setGuidelinename(String guidelinename) {
		this.guidelinename = guidelinename;
	}
	public String getGuidelineimage() {
		return guidelineimage;
	}
	public void setGuidelineimage(String guidelineimage) {
		this.guidelineimage = guidelineimage;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getGuidelineroleid() {
		return guidelineroleid;
	}
	public void setGuidelineroleid(int guidelineroleid) {
		this.guidelineroleid = guidelineroleid;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	
}